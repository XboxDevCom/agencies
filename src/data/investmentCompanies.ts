/**
 * Get all companies combined
 */
export const ALL_INVESTMENT_COMPANIES = [
  ...GAMING_COMPANIES,
  ...TECH_COMPANIES,
  ...INFRASTRUCTURE_COMPANIES,
];

/**
 * Get companies by sector
 */
export function getCompaniesBySector(
  sector: 'gaming' | 'tech' | 'infrastructure'
): InvestmentCompany[] {
  return ALL_INVESTMENT_COMPANIES.filter((c) => c.sector === sector);
}

/**
 * Get top dividend paying companies
 */
export function getTopDividendCompanies(count: number = 10): InvestmentCompany[] {
  return [...ALL_INVESTMENT_COMPANIES]
    .sort((a, b) => b.dividendYield - a.dividendYield)
    .slice(0, count);
}

/**
 * Get companies with dividend yield above threshold
 */
export function getHighDividendCompanies(
  minYield: number = 2.0
): InvestmentCompany[] {
  return ALL_INVESTMENT_COMPANIES.filter((c) => c.dividendYield >= minYield);
}
